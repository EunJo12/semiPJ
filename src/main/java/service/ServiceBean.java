package service;

public class ServiceBean {
	private int serv_num;
	private String serv_name;
	private String serv_category;
	private String region;
	private String serv_corp;
	private int price;
	private String serv_detail;
	private String filename;
	private int filesize;
	private String pwd;
	


	public int getServ_num() {
		return serv_num;
	}
	public void setServ_num(int serv_num) {
		this.serv_num = serv_num;
	}
	public String getServ_name() {
		return serv_name;
	}
	public void setServ_name(String serv_name) {
		this.serv_name = serv_name;
	}
	public String getServ_category() {
		return serv_category;
	}
	public void setServ_category(String serv_category) {
		this.serv_category = serv_category;
	}
	public String getRegion() {
		return region;
	}
	public void setRegion(String region) {
		this.region = region;
	}
	public String getServ_corp() {
		return serv_corp;
	}
	public void setServ_corp(String serv_corp) {
		this.serv_corp = serv_corp;
	}
	public int getPrice() {
		return price;
	}
	public void setPrice(int price) {
		this.price = price;
	}
	public String getServ_detail() {
		return serv_detail;
	}
	public void setServ_detail(String serv_detail) {
		this.serv_detail = serv_detail;
	}
	public String getFilename() {
		return filename;
	}
	public void setFilename(String filename) {
		this.filename = filename;
	}
	public int getFilesize() {
		return filesize;
	}
	public void setFilesize(int filesize) {
		this.filesize = filesize;
	}
	public String getPwd() {
		return pwd;
	}
	public void setPwd(String pwd) {
		this.pwd = pwd;
	}
	
	
	
	
}
