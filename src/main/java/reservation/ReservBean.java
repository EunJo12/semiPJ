package reservation;

public class ReservBean {
	private int reserv_num;
	private int serv_num;
	private String serv_date;
	private String reserv_date;
	private int reserv_price;
	private String id;
	
	public int getReserv_num() {
		return reserv_num;
	}
	public void setReserv_num(int reserv_num) {
		this.reserv_num = reserv_num;
	}
	public int getServ_num() {
		return serv_num;
	}
	public void setServ_num(int serv_num) {
		this.serv_num = serv_num;
	}
	public String getServ_date() {
		return serv_date;
	}
	public void setServ_date(String serv_date) {
		this.serv_date = serv_date;
	}
	public String getReserv_date() {
		return reserv_date;
	}
	public void setReserv_date(String reserv_date) {
		this.reserv_date = reserv_date;
	}
	public int getReserv_price() {
		return reserv_price;
	}
	public void setReserv_price(int reserv_price) {
		this.reserv_price = reserv_price;
	}
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	
	
	
}
